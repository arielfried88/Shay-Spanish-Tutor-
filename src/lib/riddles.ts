export function getRiddle(wordId: string): string {
  const riddles = RIDDLES[wordId]
  if (!riddles || riddles.length === 0) return ''
  return riddles[Math.floor(Math.random() * riddles.length)]
}

export function hasRiddle(wordId: string): boolean {
  return !!(RIDDLES[wordId]?.length)
}
